def extract_and_draw_contours(img_path, save_path, color_outer=(0,0,255), color_stroke=(0,255,0)):
    # Read the image
    img = cv2.imread(img_path)
    if img is None:
        print(f"Error: Could not load image from {img_path}")
        return False
    img_draw = img.copy()
    gray = cv2.cvtColor(img, cv2.COLOR_BGR2GRAY)
    # Binarize
    _, thresh = cv2.threshold(gray, 0, 255, cv2.THRESH_BINARY_INV + cv2.THRESH_OTSU)
    # Erosion to remove small noise
    kernel = np.ones((3,3), np.uint8)
    eroded = cv2.erode(thresh, kernel, iterations=1)
    # Extract all external contours (outer and stroke contours)
    contours, _ = cv2.findContours(eroded, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)

    # 1. Extract and draw the outermost contour
    if len(contours) > 0:
        # Combine all contours to find the overall bounding box
        all_points = np.vstack([c for c in contours if cv2.contourArea(c) > 0])
        if all_points.size > 0:
            x, y, w, h = cv2.boundingRect(all_points)
            cv2.rectangle(img_draw, (x, y), (x+w, y+h), color_outer, 2)
        else:
            print(f"Warning: No significant contours found for outer box in {img_path}")

    # 2. Extract and draw each stroke's contour
    for cnt in contours:
        if cv2.contourArea(cnt) > 10:  # Filter out small noise
            cv2.drawContours(img_draw, [cnt], -1, color_stroke, 2)

    # Save the result
    cv2.imwrite(save_path, img_draw)
    print(f"已保存标记结果到:{save_path}")
    return True

def process_and_compare_characters(student_original_img_path, standard_original_img_path):
    # Define temporary paths for processed images
    standard_processed_path = standard_original_img_path.replace('.jpg', '_processed_contours.jpg')
    student_processed_path = student_original_img_path.replace('.jpg', '_processed_contours.jpg')

    print(f"Processing standard character from {standard_original_img_path}")
    if not extract_and_draw_contours(standard_original_img_path, standard_processed_path, color_outer=(0,0,255), color_stroke=(0,255,0)):
        print("Standard character processing failed.")
        return

    print(f"Processing student character from {student_original_img_path}")
    if not extract_and_draw_contours(student_original_img_path, student_processed_path, color_outer=(255,0,0), color_stroke=(255,255,0)):
        print("Student character processing failed.")
        return

    # Load the processed images
    standard_img = cv2.imread(standard_processed_path)
    student_img = cv2.imread(student_processed_path)

    if standard_img is None:
        print(f"Error: Could not load processed standard image {standard_processed_path}")
        return
    if student_img is None:
        print(f"Error: Could not load processed student image {student_processed_path}")
        return

    # Ensure images have the same height before stacking
    h1, w1 = standard_img.shape[:2]
    h2, w2 = student_img.shape[:2]

    if h1 != h2:
        if h1 < h2:
            standard_img = cv2.resize(standard_img, (int(w1 * (h2 / h1)), h2), interpolation=cv2.INTER_AREA)
        else:
            student_img = cv2.resize(student_img, (int(w2 * (h1 / h2)), h1), interpolation=cv2.INTER_AREA)

    # Horizontally stack the images
    combined_img = np.hstack((standard_img, student_img))

    # Display the combined image using matplotlib
    plt.figure(figsize=(15, 8))
    plt.imshow(cv2.cvtColor(combined_img, cv2.COLOR_BGR2RGB))
    plt.title('Standard Character vs. Student Handwritten Character (Processed Contours)')
    plt.axis('off')
    plt.show()

    print("Combined processed contour image displayed successfully.")

def render_character_from_ttf(character, font_path, font_size, output_size=(600, 600), output_path='/content/standard_char_from_ttf.jpg'):
    """
    Renders a single character from a TTF font file into an image.

    Args:
        character (str): The character to render.
        font_path (str): Path to the TTF font file.
        font_size (int): Font size to use.
        output_size (tuple): Desired size of the output image (width, height).
        output_path (str): Path to save the rendered image.
    """
    try:
        # 3. Create a blank white background image
        img = Image.new('RGB', output_size, color = (255, 255, 255))
        # 4. Create a drawing object
        draw = ImageDraw.Draw(img)
        # 5. Load the specified TTF font and font size
        font = ImageFont.truetype(font_path, font_size)

        # 6. Calculate character text size and position to center it
        # Using textbbox to get bounding box for precise centering
        bbox = draw.textbbox((0,0), character, font=font)
        text_width = bbox[2] - bbox[0]
        text_height = bbox[3] - bbox[1]

        # Calculate position to center the text
        x = (output_size[0] - text_width) / 2 - bbox[0]
        y = (output_size[1] - text_height) / 2 - bbox[1]

        # 7. Draw the character in black
        draw.text((x, y), character, font=font, fill=(0, 0, 0))

        # 8. Save the generated image
        img.save(output_path)
        print(f"Character '{character}' rendered from '{font_path}' and saved to: {output_path}")

        # Display the image
        plt.figure(figsize=(5, 5))
        plt.imshow(img)
        plt.title(f"Standard Character: '{character}' (from TTF)")
        plt.axis('off')
        plt.show()

    except Exception as e:
        print(f"Error rendering character: {e}")

