            content = file_info['content']
            # 使用PIL加载图像
            pil_image = Image.open(io.BytesIO(content))
            original_image_pil = pil_image.copy() # 保存原始图像用于重置
            current_rotation_angle = 0 # 重置旋转角度
            current_zoom_factor = 1.0 # 重置缩放因子

