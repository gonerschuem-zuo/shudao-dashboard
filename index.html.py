original_image_pil = None # 原始上传的PIL图像，用于每次变换的基准
current_rotation_angle = 0 # 当前旋转角度
current_zoom_factor = 1.0 # 当前缩放比例

def display_image(pil_image):
    """将PIL图像转换为IPython可以显示的格式"""
    with io.BytesIO() as f:
        pil_image.save(f, format='PNG')
        display(IPImage(data=f.getvalue()))

def apply_transformations():
    """根据当前的旋转角度和缩放比例对原始图像进行变换"""
    global current_image_pil, original_image_pil, current_rotation_angle, current_zoom_factor

    if original_image_pil is None:
        return

    transformed_image = original_image_pil.copy()

    # 缩放
    if current_zoom_factor != 1.0:
        width, height = transformed_image.size
        new_width = int(width * current_zoom_factor)
        new_height = int(height * current_zoom_factor)
        # 使用Image.Resampling.LANCZOS (PIL.Image.LANCZOS) 进行高质量缩放
        transformed_image = transformed_image.resize((new_width, new_height), Image.Resampling.LANCZOS)

    # 旋转
    if current_rotation_angle != 0:
        # 使用Image.Resampling.BICUBIC (PIL.Image.BICUBIC) 进行高质量旋转，并保持透明背景（如果有）
        transformed_image = transformed_image.rotate(current_rotation_angle, expand=True, fillcolor=(255, 255, 255))

    current_image_pil = transformed_image
    with output_area:
        clear_output(wait=True)
        display_image(current_image_pil)

